// As variáveis são "caixinhas" onde voçê guarda valores/informações para usar depois no seu código.

// No javascript, temos 3 formas principais de declarar varíaveis

// 1 - var - forma mais antiga de se declarar varíaveis
// var nome - "Mago";
// var nome - "Teste"

// 2 - let
let nome = "Mago"
console.log(nome);

nome = "Aragorn"
console.log(nome)


// 3 - const
const sobrenome = "Gandalf"
console.log(sobrenome)

sobrenome ='Teste'
